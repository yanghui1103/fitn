package com.bw.fit.flow.model;

public class Node {
	private String node_code ;
	private String node_name;
	private String node_dealers;
	private String node_dealernames;
	
	
	
	public String getNode_code() {
		return node_code;
	}
	public void setNode_code(String node_code) {
		this.node_code = node_code;
	}
	public String getNode_name() {
		return node_name;
	}
	public void setNode_name(String node_name) {
		this.node_name = node_name;
	}
	public String getNode_dealers() {
		return node_dealers;
	}
	public void setNode_dealers(String node_dealers) {
		this.node_dealers = node_dealers;
	}
	public String getNode_dealernames() {
		return node_dealernames;
	}
	public void setNode_dealernames(String node_dealernames) {
		this.node_dealernames = node_dealernames;
	}
	
	
	
	
}
